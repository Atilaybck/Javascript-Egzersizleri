// Bir dizideki elemanları tersine çeviren bir fonksiyon yazın.

function tersCevir(dizi) {
    return dizi.reverse();
}

// Kullanım örneği:
let meyveler = ["elma", "armut", "kiraz", "çilek"];
console.log(tersCevir(meyveler)); // Beklenen çıktı: ["çilek", "kiraz", "armut", "elma"]