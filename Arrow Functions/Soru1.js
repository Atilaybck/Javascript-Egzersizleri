//Bir arrow function oluşturarak, iki parametre alan ve bu parametreleri toplayan basit bir fonksiyon yazın.


// Arrow function kullanarak iki parametreyi toplayan fonksiyon
const topla = (parametre1, parametre2) => parametre1 + parametre2;

// Örnek kullanım
const sonuc = topla(5,12);
const sonuciki = topla(12,12);
console.log(sonuc);
console.log(sonuciki);
