//  Bir JavaScript programı yazarak, 1'den 100'e kadar olan sayıları toplamını bulun ve ekrana yazdırın.

// Toplamı hesapla
var toplam = 0;
for (var i = 1; i <= 100; i++) {
    toplam += i;
}

// Toplamı ekrana yazdır
console.log("1'den 100'e kadar olan sayıların toplamı:", toplam);